    /**
     * 计算日期间隔
     *
     * @return 返回差值天数
     */
    public static int timeCalculater(String star, String en) { // 计算日期长度
        SimpleDateFormat df = new SimpleDateFormat("yyyyMMdd");
        try {
            Date d1 = df.parse(star);
            Date d2 = df.parse(en);
            long militime = d2.getTime() - d1.getTime();
            return (int) (militime / (1000 * 3600 * 24));
        } catch (Exception e) {
            e.printStackTrace();
            return -1;
        }
    }

    /**
     * 计算时间long数组中的最大值
     *
     * @return 最大值的位置
     */
    public static int timeMaxPosition(String[] time, int length) { // 计算日期长度
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        Date[] date = new Date[length];
        long[] millitime = new long[length];
        for (int i = 0; i < length; i++) {
            try {
                date[i] = df.parse(time[i]);
            } catch (ParseException e) {
                e.printStackTrace();
            }
            millitime[i] = date[i].getTime();
        }

        long maxtime = millitime[0];
        int position = 1;
        int maxposition = 0;
        for (position = 1; position < millitime.length; position++) {
            if (millitime[position] > maxtime) {
                maxtime = millitime[position];
                maxposition = position;
            }
        }
        return maxposition;
    }

